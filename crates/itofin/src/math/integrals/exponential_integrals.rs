/// Auxiliary function `f` for the large-argument asymptotics of `Si`/`Ci`.
fn f_aux(x: Real) -> Real {
    let w = 1.0 / (x * x);
    horner(w, &F_NUM) / (x * horner(w, &F_DEN))
}

