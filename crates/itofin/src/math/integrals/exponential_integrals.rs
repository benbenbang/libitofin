/// Auxiliary function `g` for the large-argument asymptotics of `Si`/`Ci`.
fn g_aux(x: Real) -> Real {
    let w = 1.0 / (x * x);
    w * horner(w, &G_NUM) / horner(w, &G_DEN)
}

